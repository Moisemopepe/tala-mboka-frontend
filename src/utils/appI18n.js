export function getStoredLanguage() {
  return localStorage.getItem("tala_public_language") || localStorage.getItem("tala_admin_language") || "en";
}

export function setStoredLanguage(language) {
  localStorage.setItem("tala_public_language", language);
  window.dispatchEvent(new CustomEvent("tala:language-changed", { detail: language }));
}

export function getAppCopy(language) {
  return appCopy[language] || appCopy.en;
}
